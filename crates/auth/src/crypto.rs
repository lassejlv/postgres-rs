//! Minimal, dependency-free cryptographic primitives needed for SCRAM-SHA-256
//! authentication: SHA-256, HMAC-SHA-256, PBKDF2-HMAC-SHA-256, Base64, and a
//! source of random bytes.
//!
//! These are standard, well-specified algorithms (FIPS 180-4, RFC 2104,
//! RFC 8018, RFC 4648); the implementations are kept compact and are covered
//! by the published test vectors in the unit tests below.

use std::fs::File;
use std::io::Read;

